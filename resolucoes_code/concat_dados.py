# Vamos receber dois dados diferentes do usuário e concatená-los em uma única string.

info1 = input("Digite a primeira informação: ")
info2 = input("Digite a segunda informação: ")

# Concatenar significa unir, no caso vamos unir duas strings em uma só.

info_concatenada = info1 + " " + info2

# Para unir as duas variáveis, usamos o sinal de "+" que no caso, está unindo também um espaço entre as strings.

print("As informações concatenadas são: ", info_concatenada)
