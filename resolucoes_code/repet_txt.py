# Vamos solicitar uma string e um numero inteiro como entrada. 
# Depois teremos que retornar a string repetida o número de vezes informado.

# Entrando com a string.

str = input("Entre com a string: ")

# Adicionando espaços concatenando com a string.

string = str + " "

# Entrando com o numero de repetições da string.

num = int (input ("Entre com um número inteiro qualquer: "))

# Realizando as repetições e printando.

print(string * num)
